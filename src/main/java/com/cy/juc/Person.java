package com.cy.juc;

public class Person {
    private Integer id;
    private String name;
    public Person(){}


    public Person(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
